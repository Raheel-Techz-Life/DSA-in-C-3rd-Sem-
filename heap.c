#include <stdio.h>
#include <stdlib.h>

void swap(int* a, int* b) {
    int temp = *a;
    *a = *b;
    *b = temp;
}

void heapify(int arr[], int n, int i) {
    int largest = i;
    int left = 2 * i + 1;
    int right = 2 * i + 2;
    
    if (left < n && arr[left] > arr[largest]) {
        largest = left;
    }
    
    if (right < n && arr[right] > arr[largest]) {
        largest = right;
    }
    
    if (largest != i) {
        swap(&arr[i], &arr[largest]);
        heapify(arr, n, largest);
    }
}

void buildMaxHeap(int arr[], int n) {
    for (int i = n / 2 - 1; i >= 0; i--) {
        heapify(arr, n, i);
    }
}

void heapSort(int arr[], int n) {
    buildMaxHeap(arr, n);
    
    for (int i = n - 1; i > 0; i--) {
        swap(&arr[0], &arr[i]);
        heapify(arr, i, 0);
    }
}

void printArray(int arr[], int n) {
    for (int i = 0; i < n; i++) {
        printf("%d ", arr[i]);
    }
    printf("\n");
}

int main() {
    printf("Heap Operations Demo\n");
    printf("===================\n\n");
    
    int arr[] = {12, 11, 13, 5, 6, 7};
    int n = 6;
    
    printf("Original array: ");
    printArray(arr, n);
    
    buildMaxHeap(arr, n);
    printf("Max Heap: ");
    printArray(arr, n);
    
    int arr2[] = {12, 11, 13, 5, 6, 7};
    heapSort(arr2, n);
    printf("After Heap Sort: ");
    printArray(arr2, n);
    
    return 0;
}
